package baekjun;

import java.util.Scanner;

public class b2742 {
	//해결 ㅇ while문으로도 가능
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int input = sc.nextInt();
		
		for(int i=input;i>0;i--) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
